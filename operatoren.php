<?php
	# >>Arithmetische Operatoren<<
	# Addition, Subtraktion, Multiplikation, Division, das sind die arithmetischen Operationen,
	# die Sie aus dem Matheunterricht kennen. Sie lassen sich in PHP ganz einfach einsetzen:
	
	$a = 7;
	$b = 3;
	$erg = $a * $b;
	echo $erg;
	
	# Operator		Beispiel				Beschreibung
	#	+			$erg = 7 + 3; //10		Addition zweier Zahlen
	#	-			$erg = 7 - 3; //4		Subtraktion zweier Zahlen
	#	*			$erg = 7 * 3; //21		Multiplikation zweier Zahlen
	#	/			$erg = 7 / 3; //2.33	Division zweier Zahlen
	#	%			$erg = 7 % 3; //1		Berechnet den ganzzahligen Rest einer Division.
?>
