<?php
	# Anführungszeichen
	# Um doppelte oder einfache Anführungszeichen zu verwenden, müssen die jeweils 
	# andere Anführungszeichen-Art einsetzen, um die Ausgabe zu begrenzen:
	echo 'Er sagte: "Ich denke, also bin ich!"';
	
	# Wenn einfache und doppelte Anführungszeichen in einem String verwendet werden möchten,
	# müssen die jeweiligen Anführungszeichen per Backslash entwertet werden:
	echo 'McDonald\'s-Esser: "Ich liebe nichts!"';
?>